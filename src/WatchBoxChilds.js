
export function Summary({ watched }) {
  const average = (arr) =>
    arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);
  const avgImdbRating = average(watched.map((movie) => movie.imdbRating));
  const avgUserRating = average(watched.map((movie) => movie.userRating));
  const avgRuntime = average(watched.map((movie) => movie.runtime));

  return (
    <div className="summary">
      <h2>Movies you've watched</h2>
      <div>
        <p>
          <span>#Ô∏è‚É£</span>
          <span>{watched.length} movies</span>
        </p>
        <p>
          <span>‚≠êÔ∏è</span>
          <span>{avgImdbRating.toFixed(1)}</span>
        </p>
        <p>
          <span>üåü</span>
          <span>{avgUserRating.toFixed(1)}</span>
        </p>
        <p>
          <span>‚è≥</span>
          <span>{avgRuntime.toFixed(2)} min</span>
        </p>
      </div>
    </div>
  );
}

export function WatchedMovieList({ watched, onClearMovie }) {
  return (
    <ul className="list">
      {watched.map((movie) => (
        <WatchedMovie
          key={movie.imdbID}
          movie={movie}
          watched={watched}
          onClearMovie={onClearMovie}
        />
      ))}
    </ul>
  );
}

function WatchedMovie({ movie, onClearMovie }) {
  const hasPoster = movie.poster !== "N/A";
  return (
    <li key={movie.imdbID}>
      {hasPoster ? (
        <img src={movie.poster} alt={`${movie.title}`} />
      ) : (
        <div style={{ gridRow: "1 / -1", margin: "auto" }}>üçø</div>
      )}
      <h3>{movie.title}</h3>
      <button className="btn-delete" onClick={() => onClearMovie(movie.imdbID)}>
        <span>X</span>
      </button>
      <div>
        <p>
          <span>‚≠êÔ∏è</span>
          <span>{movie.imdbRating}</span>
        </p>
        <p>
          <span>üåü</span>
          <span>{movie.userRating}</span>
        </p>
        <p>
          <span>‚è≥</span>
          <span>{movie.runtime} min</span>
        </p>
      </div>
    </li>
  );
}